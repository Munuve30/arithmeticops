#Arithmetic(+,-,/,//,*,%) with  integers
#function of / & //
a = 34
c = 8
print(a/c)
print(a//c)
# / - includes decimal numbers
# // - doesnot include decimal numbers

#function of +, -, * & %
a = 45
b = 34
print(a+b)
print(b-a)
print(a-b)
print(a*b)
print(a%b)

#Arithmetic(+,-,/,//,*,%) with  strings
#only + operatore is possible with strings. - , / , , // , * , % aren`t possible
x = "Joshua"
y = "Musyimi"
print(x+y)

#Arithmetic(+,-,/,//,*,%) with  strings
#all arithmetic operators possible
t = 40.4
s = 1.2
print(t+s)
print(t-s)
print(t*s)
print(t%s)
print(t/s)

